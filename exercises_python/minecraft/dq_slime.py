import mcpi.minecraft as minecraft
import mcpi.block as block
import numpy as np


def build_block(colorArray):
    mc = minecraft.Minecraft.create()
    basePos = mc.player.getTilePos()

    basePos.x = basePos.x + slime.shape[0] + 2
    basePos.z = basePos.z - (slime.shape[1] / 2)

    for row in range(colorArray.shape[0]):
        for column in range(colorArray.shape[1]):

            colorNumber = colorArray[colorArray.shape[0] - row - 1][column]
            if colorNumber == None:
                continue

            mc.setBlock(
                basePos.x,
                basePos.y + row,
                basePos.z + column,
                block.WOOL.id,
                colorNumber)

